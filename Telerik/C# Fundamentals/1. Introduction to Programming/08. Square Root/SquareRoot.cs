/*
 Write a program that calculates the square root of the number 12345 and prints it on the console.
 */

using System;

class SquareRoot
{
    static void Main()
    {
        int number = 12345;
        double squareRoot = Math.Sqrt(number);

        Console.WriteLine(squareRoot);
    }
}

